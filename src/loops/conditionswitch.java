package loops;

import java.util.Scanner;

public class conditionswitch {
	
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		
		
		for(int i = 1; i<=10;  i++) {
			if(i==5  || i==6)  {
			continue;
			}
			
			System.out.println(i);
			
			sc.close();
	}

}
	}
